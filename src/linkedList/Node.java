package linkedList;

	public class Node {

		private Object data;
		protected Node next;
		

		public Node(Object data) {
			this.data = data;
			this.next = null;
		}
		
		public Node getNext() {
			return next;
		}
		public Object getData() {
			return data;
		}
		public int getSize() {
			return this.getSize();
		}
		public void setNext(Node obj) {
			this.next = obj;
		}
	}
		
